def patch_translations() -> None:
    """Fügt die Employee-Übersetzungen in das bestehende TRANSLATIONS-Dict ein.

    Wird einmalig beim Route-Import aufgerufen. Überschreibt keine bestehenden Keys.
    """
    from i18n import TRANSLATIONS

    for lang, keys in EMPLOYEE_TRANSLATIONS.items():
        if lang not in TRANSLATIONS:
            TRANSLATIONS[lang] = {}
        for key, value in keys.items():
            if key not in TRANSLATIONS[lang]:
                TRANSLATIONS[lang][key] = value
