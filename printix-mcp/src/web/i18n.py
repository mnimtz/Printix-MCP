# ─── Spracherkennung ──────────────────────────────────────────────────────────

def detect_language(accept_language: str | None) -> str:
    """
    Erkennt die bevorzugte Sprache aus dem Accept-Language-Header.
    Gibt einen der unterstützten Sprachcodes zurück.

    Beispiel: "de-DE,de;q=0.9,en;q=0.8" → "de"
    """
    if not accept_language:
        return DEFAULT_LANGUAGE

    for part in accept_language.split(","):
        lang = part.split(";")[0].strip().lower()
        # Erst exakt prüfen (z.B. "de")
        if lang in SUPPORTED_LANGUAGES:
            return lang
        # Dann Prefix prüfen (z.B. "de-AT" → "de")
        prefix = lang[:2]
        if prefix in SUPPORTED_LANGUAGES:
            return prefix

    return DEFAULT_LANGUAGE


def get_translations(lang: str) -> dict[str, str]:
    """
    Gibt das Übersetzungsdict für den angegebenen Sprachcode zurück.
    Fällt bei unbekannter Sprache auf Englisch zurück.
    """
    return TRANSLATIONS.get(lang, TRANSLATIONS[DEFAULT_LANGUAGE])


def make_translator(lang: str):
    """
    Gibt eine _(key) Funktion zurück, die Übersetzungen für `lang` liefert.
    Falls ein Key fehlt, wird der englische Fallback verwendet; ist auch dieser
    nicht vorhanden, wird der Key selbst zurückgegeben.
    """
    t  = get_translations(lang)
    en = TRANSLATIONS[DEFAULT_LANGUAGE]

    def _(key: str, **kwargs) -> str:
        text = t.get(key) or en.get(key) or key
        if kwargs:
            try:
                text = text.format(**kwargs)
            except (KeyError, ValueError):
                pass
        return text

    return _


